import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parseExportedMetaFromComponentSource } from "./script-utils/hobbit-registry-ts-utils.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..", "..", "..");

const COMPONENTS_DIR = path.join(repoRoot, "src", "hobbit-library", "components");
const REGISTRY_OUT = path.join(repoRoot, "src", "hobbit-library", "registry.ts");

function safeReadFile(filePath) {
  try {
    return fs.readFileSync(filePath, "utf8");
  } catch {
    return null;
  }
}

/** @returns {string[]} absolute paths, sorted */
function collectTsxFilesRecursive(dir) {
  /** @type {string[]} */
  const out = [];
  if (!fs.existsSync(dir)) return out;

  function walk(d) {
    for (const ent of fs.readdirSync(d, { withFileTypes: true })) {
      const p = path.join(d, ent.name);
      if (ent.isDirectory()) {
        // Dev-only preview shell and demo primitives — not part of the public component registry.
        if (ent.name === "meta-preview") continue;
        walk(p);
      } else if (ent.isFile() && ent.name.endsWith(".tsx")) out.push(p);
    }
  }
  walk(dir);
  return out.sort((a, b) => a.localeCompare(b));
}

/**
 * @param {{ name: string, description?: string, variants?: string[] }} meta
 */
function formatMetaBlock(meta) {
  const lines = ["    meta: {", `      name: ${JSON.stringify(meta.name)},`];
  if (meta.description !== undefined) {
    lines.push(`      description: ${JSON.stringify(meta.description)},`);
  }
  if (meta.variants?.length) {
    lines.push(`      variants: [${meta.variants.map((v) => JSON.stringify(v)).join(", ")}],`);
  }
  lines.push("    },");
  return lines.join("\n");
}

/**
 * @param {{ filePath: string, meta: { name: string, description?: string, variants?: string[] } }[]} entries
 */
function formatRegistryFile(entries) {
  const blocks = entries.map(
    (e) =>
      `  {\n    filePath: ${JSON.stringify(e.filePath)},\n${formatMetaBlock(e.meta)}\n  },`,
  );
  const arrayBody = blocks.length ? `\n${blocks.join("\n")}\n` : "\n";

  return `/**
 * AUTO-GENERATED by \`npm run hobbit-library:sync-registry\` (also runs at the start of \`npm run hobbit-library:docs\`).
 * Do not edit the \`registry\` array by hand. Add \`export const meta\` to a reusable component under \`src/hobbit-library/components/\` (the \`meta-preview/\` folder is skipped).
 */

export type HobbitLibraryComponentMeta = {
  name: string;
  description?: string;
  variants?: string[];
};

export type HobbitLibraryRegistryEntry = {
  /**
   * Path to the component file relative to repo root, e.g. \`src/hobbit-library/components/ButtonPrimary.tsx\`.
   * Used by docs tooling and kept in sync by the sync script.
   */
  filePath: string;
  meta: HobbitLibraryComponentMeta;
};

export const registry: HobbitLibraryRegistryEntry[] = [${arrayBody}];
`;
}

function main() {
  const tsxPaths = collectTsxFilesRecursive(COMPONENTS_DIR);
  /** @type {{ filePath: string, meta: { name: string, description?: string, variants?: string[] } }[]} */
  const entries = [];
  let skipped = 0;

  for (const abs of tsxPaths) {
    const source = safeReadFile(abs);
    if (!source) continue;
    const meta = parseExportedMetaFromComponentSource(source);
    if (!meta) {
      skipped += 1;
      continue;
    }
    const filePath = path.relative(repoRoot, abs).split(path.sep).join("/");
    entries.push({ filePath, meta });
  }

  entries.sort((a, b) => a.filePath.localeCompare(b.filePath));

  const text = formatRegistryFile(entries);
  fs.mkdirSync(path.dirname(REGISTRY_OUT), { recursive: true });
  fs.writeFileSync(REGISTRY_OUT, text, "utf8");

  const rel = path.relative(repoRoot, REGISTRY_OUT);
  console.log(`Wrote ${rel} (${entries.length} entries, ${skipped} .tsx files without export const meta)`);
}

main();
